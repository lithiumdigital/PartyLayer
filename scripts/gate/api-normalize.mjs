/**
 * AST-normalized canonical form for .d.ts public-API snapshots.
 *
 * The previous approach diffed Prettier-formatted TEXT of the tsup-rolled
 * dist/index.d.ts. Prettier normalizes ITS formatting but cannot erase a
 * structural form difference in its INPUT — so an environment-sensitive d.ts
 * rollup (e.g. a consistent indentation offset / import wrapping) produced a
 * large FALSE drift (local ≠ CI) while a real API change and a cosmetic one
 * were indistinguishable.
 *
 * This module derives an ENVIRONMENT-STABLE canonical from the parse tree:
 *   parse → reprint each top-level declaration via the TS printer with comments
 *   removed (source indentation/whitespace/wrapping and comment text cannot
 *   register) → collapse residual whitespace → SORT (export ORDER is not a
 *   public contract). `declare module {…}` wrappers are flattened so a wrapped
 *   vs flat rollup compares equal. Private class members are retained (the
 *   printer emits them), so a new `private foo()` still flags.
 *
 * ESCALATION (NOT built — YAGNI, but real): tsup can emit a genuinely different
 * declaration STRUCTURE for the same symbol — e.g. an inlined `interface X {…}`
 * vs `export { X } from '@partylayer/core'`. Re-export form ALREADY exists in
 * this tree (main's committed sdk snapshot is re-export form), so this variance
 * is not hypothetical. Flattening + reprint handles whitespace/indent/wrapping,
 * but a true inline↔re-export flip for the SAME symbol would still show as
 * different declaration text → false drift. If that ever happens, the
 * form-immune escalation is type-checker RESOLUTION: build a `ts.Program` over
 * the entry .d.ts and emit `checker.typeToString(...)` for each resolved export
 * (+ class members). It's heavier (module resolution, a Program per entry) and
 * unnecessary today — add it only when an inline↔re-export flip actually bites.
 */
import ts from 'typescript';

/**
 * Schema marker for the canonical FORM. Bump when the normalization changes
 * (e.g. a TypeScript upgrade shifts `ts.createPrinter` output) so a mass
 * canonical change is diagnosable from the header version-stamp rather than a
 * mystery — the snapshot analog of stamping a provenance/version on an artifact.
 */
export const SNAPSHOT_SCHEMA = 'ast-normalized-v1';
export const TS_VERSION = ts.version;

const HEADER_SEP =
  '// ----------------------------------------------------------------------------';

/** The informational, NON-compared header (carries the TS version stamp). */
export function buildHeader(epName) {
  return (
    `// API SURFACE SNAPSHOT — ${epName}\n` +
    `// AST-normalized: strip comments → reprint each top-level declaration →\n` +
    `// collapse whitespace → sort. Immune to comments / formatting / declaration\n` +
    `// order; still flags any real type change (symbols, signatures, members\n` +
    `// including private). Order is NOT a public contract, so it is sorted away.\n` +
    `// Schema: ${SNAPSHOT_SCHEMA} · TypeScript ${TS_VERSION}\n` +
    `// Generated by scripts/gate/api-snapshot.mjs. DO NOT edit by hand.\n` +
    `// To intentionally accept a public-API change, run: pnpm gate:api:update\n` +
    `${HEADER_SEP}\n`
  );
}

/**
 * The COMPARED body of a committed snapshot = everything after the header
 * separator. The header (incl. the TS version stamp) is informational and is
 * NOT part of the comparison, so a TS patch bump that does not change the
 * canonical never causes false drift; a canonical change that IS caused by a TS
 * upgrade is then diagnosable via the stamped version.
 */
export function snapshotBody(text) {
  const i = text.indexOf(HEADER_SEP);
  if (i === -1) return text;
  return text.slice(i + HEADER_SEP.length).replace(/^\r?\n/, '');
}

/** Flatten `declare module {…}` wrappers so wrapped vs flat rollups compare equal. */
function flattenStatements(statements) {
  const out = [];
  for (const stmt of statements) {
    if (ts.isModuleDeclaration(stmt) && stmt.body && ts.isModuleBlock(stmt.body)) {
      out.push(...flattenStatements(stmt.body.statements));
    } else {
      out.push(stmt);
    }
  }
  return out;
}

/**
 * Environment-stable canonical of a .d.ts public surface — see module header.
 * Returns the sorted, comment-free, whitespace-collapsed declaration text.
 */
export function astCanonical(rawDts) {
  const src = ts.createSourceFile(
    'snapshot.d.ts',
    rawDts,
    ts.ScriptTarget.Latest,
    /* setParentNodes */ true,
  );
  const printer = ts.createPrinter({
    removeComments: true,
    newLine: ts.NewLineKind.LineFeed,
  });
  const decls = flattenStatements(src.statements).map((stmt) =>
    printer.printNode(ts.EmitHint.Unspecified, stmt, src).replace(/\s+/g, ' ').trim(),
  );
  decls.sort();
  // Drop any empties (e.g. stray semicolons) for stability.
  return decls.filter((d) => d.length > 0).join('\n') + '\n';
}
